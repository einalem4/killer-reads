// Precompiles views/**/*.handlebars into plain JS (no eval/new Function), since
// Cloudflare Workers does not allow dynamic code generation at runtime and
// Handlebars.compile() relies on it. Run automatically before dev/deploy
// (see package.json "predev"/"predeploy"), or manually via `npm run build:templates`
// after editing any .handlebars file.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import Handlebars from 'handlebars';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const viewsDir = path.join(root, 'views');
const outDir = path.join(root, 'src', 'generated');

const VIEWS = [
  'landing',
  'login',
  'signup',
  'forgot-password',
  'reset-password',
  'create-post',
  'edit-post',
  'discussions',
  'single-post',
  'user-profile',
  'images'
];

const PARTIALS = {
  comments: 'partials/comments',
  'post-info': 'partials/post-info'
};

function precompile(relativePath) {
  const source = readFileSync(path.join(viewsDir, `${relativePath}.handlebars`), 'utf8');
  return Handlebars.precompile(source);
}

mkdirSync(outDir, { recursive: true });

let out = '';
out += '// AUTO-GENERATED by scripts/build-templates.mjs\n';
out += '// Do not edit by hand -- edit the .handlebars source in views/ and re-run `npm run build:templates`.\n\n';

out += `export const layoutSpec = ${precompile('layouts/main')};\n\n`;

out += 'export const viewSpecs = {\n';
for (const name of VIEWS) {
  out += `  ${JSON.stringify(name)}: ${precompile(name)},\n`;
}
out += '};\n\n';

out += 'export const partialSpecs = {\n';
for (const [name, relativePath] of Object.entries(PARTIALS)) {
  out += `  ${JSON.stringify(name)}: ${precompile(relativePath)},\n`;
}
out += '};\n';

writeFileSync(path.join(outDir, 'templates.js'), out);
console.log(`Precompiled ${VIEWS.length} views + ${Object.keys(PARTIALS).length} partials + layout -> src/generated/templates.js`);
